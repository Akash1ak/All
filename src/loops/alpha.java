package loops;

public class alpha {
    public static void main(String[] args) {

        char alpha = 'X';

        if (alpha >= 'a' && alpha <= 'z')
            System.out.println(alpha + " is an lower alphabet.");
        else if (alpha >= 'A' && alpha <= 'Z')
            System.out.println(alpha + " is an Uppercase an alphabet.");
    }
}
